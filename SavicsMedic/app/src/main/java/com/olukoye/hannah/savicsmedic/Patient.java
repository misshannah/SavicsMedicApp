package com.olukoye.hannah.savicsmedic;

public class Patient {

    String fullname;
    String email;
    int age;
    char gender;


    Patient(String fullname, String email, int age, char gender) {
        this.fullname = fullname;
        this.email = email;
        this.age = age;
        this.gender = gender;
    }
}
